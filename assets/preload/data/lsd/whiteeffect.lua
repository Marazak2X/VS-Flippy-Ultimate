function onBeatHit()
     if curBeat == 16 then
        effectON()
        makeLuaSprite('whiteLOL1', '', -600,-200)
        makeGraphic('whiteLOL1', 3000, 2700, '000000')
        setProperty('whiteLOL1.alpha', 0.4)
        setScrollFactor('whiteLOL1', 0,0)
        addLuaSprite('whiteLOL1', true)
     end
end

